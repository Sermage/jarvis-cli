"""LLM-переформулировщик запроса — реализация порта `app.ports.QueryRewriter`.

Первый этап улучшенного RAG: короткий/разговорный вопрос пользователя
превращается в развёрнутый поисковый запрос (раскрытые аббревиатуры, добавленные
термины и синонимы, в т.ч. английские), чтобы его эмбеддинг лучше «попадал» в
релевантные чанки. При любой ошибке или пустом ответе возвращается исходный
запрос — переформулировка не должна ломать поиск.
"""
from __future__ import annotations

from typing import Any

_REWRITE_SYSTEM = (
    "Ты помогаешь искать по технической базе знаний проекта (код и документация). "
    "Переформулируй вопрос пользователя в развёрнутый поисковый запрос: раскрой "
    "аббревиатуры, добавь ключевые технические термины и синонимы (в том числе "
    "английские названия функций/классов, если уместно), убери вводные слова и "
    "вежливость. Сохрани смысл. Верни ТОЛЬКО переформулированный запрос одной "
    "строкой, без пояснений и кавычек."
)


class LLMQueryRewriter:
    """Переформулировка запроса через LLM (один вызов на запрос)."""

    def __init__(self, client: Any, params: dict):
        self._client = client
        self._params = params

    def rewrite(self, query: str) -> str:
        try:
            reply = self._client.chat(
                [{"role": "user", "content": query}], self._params, _REWRITE_SYSTEM)
        except Exception:
            return query
        if not reply:
            return query
        # берём первую непустую строку — на случай, если модель добавила лишнее
        for line in reply.strip().splitlines():
            line = line.strip().strip('"').strip()
            if line:
                return line
        return query
