"""ANSI escape-коды для цветного вывода в терминал."""

RESET   = "\033[0m"
BOLD    = "\033[1m"
DIM     = "\033[2m"
CYAN    = "\033[36m"
GREEN   = "\033[32m"
RED     = "\033[31m"
YELLOW  = "\033[33m"
MAGENTA = "\033[35m"
BLUE    = "\033[34m"

# Очистка экрана + скроллбэка и переход курсора в (1,1). Используется при /clear,
# чтобы новая сессия начиналась в чистом окне терминала.
CLEAR_SCREEN = "\033[H\033[2J\033[3J"
